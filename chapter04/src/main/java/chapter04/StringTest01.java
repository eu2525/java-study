package chapter04;

public class StringTest01 {

	public static void main(String[] args) {
		// c:\temp
		System.out.println("c:\\temp"); // \t 로 하면 tab이라는 의미가 되므로 \\처럼 \를 2번 적어줘야함.
		
		// \t : tab , \r : carriage return , \n : newline
		// "hello" 출력하기
		System.out.println("\"hello\"");
		// ' 출력하기
		char c = '\'';
		System.out.println(c);
	}

}
